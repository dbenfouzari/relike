class FontWeight {
  /**
   * Thin, the least thick.
   */
  static w100 = 100;
  /**
   * Extra-light.
   */
  static w200 = 200;
  /**
   * Light.
   */
  static w300 = 300;
  /**
   * Normal / regular / plain.
   */
  static w400 = 400;
  /**
   * Medium.
   */
  static w500 = 500;
  /**
   * Semi-bold.
   */
  static w600 = 600;
  /**
   * Bold.
   */
  static w700 = 700;
  /**
   * Extra-bold.
   */
  static w800 = 800;
  /**
   * Black, the most thick.
   */
  static w900 = 900;

  static thin = FontWeight.w100;

  static extraLight = FontWeight.w200;
  static light = FontWeight.w300;
  /**
   * The default font weight.
   */
  static normal = FontWeight.w400;
  static medium = FontWeight.w500;
  static semiBold = FontWeight.w600;
  /**
   * A commonly used font weight that is heavier than normal.
   */
  static bold = FontWeight.w700;
  static extraBold = FontWeight.w800;
  static black = FontWeight.w900;
}

export default FontWeight;
